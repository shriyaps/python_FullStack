# Example for complex datatype

## Complex numbers a+bj

com_1 = 1+3j
print(com_1)
print("The data type for the com_1 is: ", type(com_1))

com_2 = 8.5-5.6j
print(com_2)
print("The data type for the com_2 is: ", type(com_2))

com_3 = -90.5+56.47j
print(com_3)
print("The data type for the com_3 is: ", type(com_3))

com_4 = 0+56.47j
print(com_4)
print("The data type for the com_4 is: ", type(com_4))
