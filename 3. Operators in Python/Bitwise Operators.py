# Examples for Bitwise Operators
# (-x-1)

x = 10; y = 11

print("Bitwise Complement of x: ", ~x)

print("Bitwise AND of x and y: ", x & y)

print("Bitwise OR of x and y: ", x | y)

print("Bitwise XOR of x and y: ", x ^ y)

print("Bitwise Left Shift of x by 2 positions: ", x << 2)

print("Bitwise Right Shift of x by 2 positions: ", x >> 2)
